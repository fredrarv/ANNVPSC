def generate_line(i):
    R = Rotation.to_euler(Rotation.random(1000))
    return f"{R[i,0]*180/pi:8.2f} {R[i,1]*180/pi:8.2f} {R[i,2]*180/pi:8.2f}  1.0"

def generate_file(filename, num_lines):
    with open(os.path.join(VPSC_directory,filename), 'w') as f:
        # Write the header at the top of the file
        f.write(f"dummy\ndummy\nrandom texture generated by RANDTEXT.PY\nB   {num_lines}\n")
        
        # Write the specified number of random lines
        for i in range(num_lines):
            f.write(generate_line(i) + "\n")

# Generate n lines
